    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $authors = new Author();
        $authors->name = $request->name;
        $authors->email = $request->email;
        $authors->bio = $request->bio;

        if ($request->file('profile') == null) {
            $authors->image_path = 'default.jpg';
        } else {
            $ext = $request->file('profile')->extension();
            $final_name = date('YmdHis') . '.' . $ext;
            $request->file('profile')->move(public_path('profile/'), $final_name);
            $authors->image_path = $final_name;
        }
        $authors->save();
        return redirect()->back();
    }

